	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=VAL1
	LDR R1,=VAL2
	LDR R2,=RESULT
	LDR R3,[R0]
	LDR R4,[R1]
	UMULL R5,R6,R3,R4
	STR R5,[R2]
	ADD R2,#4
	STR R6,[R2]
STOP
	B STOP
VAL1 DCD 1
VAL2 DCD 2
	AREA mydata,DATA,READWRITE
RESULT DCD 0
	END