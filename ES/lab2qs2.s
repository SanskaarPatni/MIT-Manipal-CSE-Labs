	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R1,=VAL1
	LDR R2,=VAL2
	LDR R3,=DST
	MOV R4,#0
	MOV R5,#4
LOOP LDR R6,[R1]
	LDR R7,[R2]
	ADDS R6,R4
	MOV R4,#0
	ADC R4,#0
	ADDS R6,R7
	ADC R4,#0
	ADD R1,#4
	ADD R2,#4
	SUBS R5,#1
	STR R6,[R3]
	ADD R3,R3,#4
	BNE LOOP
	STR R4,[R3]
STOP
	B STOP
VAL1 DCD 0XFFFFFFFF,0XF,0XF,0XF
VAL2 DCD 2,2,2,2
	AREA mydata,DATA,READWRITE	
DST DCD 0
	END
	
	