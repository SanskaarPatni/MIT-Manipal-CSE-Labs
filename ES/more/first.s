	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=V1
	LDR R1,[R0]
	LDR R6,=RESULT
	MOV R2,#0

up  CMP R1,#0XA
	BCC STORE
	SUB R1,#0XA
	ADD R2,R2,#1
	B up

STORE ADD R1,#0X30
	STRB R1,[R6],#1
	MOV R1,R2
	MOV R2,#0
	CMP R1,#0XA
	BCS up
	ADD R1,#0X30
	STRB R1,[R6]
	
STOP B STOP
V1 DCD 0XAC
	AREA mydata,DATA,READWRITE
RESULT DCD 0
	END
	