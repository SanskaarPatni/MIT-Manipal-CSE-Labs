	AREA    RESET, DATA, READONLY
     	EXPORT  __Vectors

__Vectors 
	DCD  0x40001000     ; stack pointer value when stack is empty
      DCD  Reset_Handler  ; reset vector
  
    	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
		
Reset_Handler

	LDR R0,=INPUT      ; R0 HOLDING INPUT ARRAY BASE ADDRESS
	LDR R1,=POSITION   ; RESULT WRITTING SPACE
	MOV R3, #10         ; COUNTER FOR LOOP
	MOV R4, #76         ; ELEMENT TO BE SEARCHED
	MOV R6, #1			; INDICATOR VARIABLE FOR PRESENCE OF ELEMENT
	MOV R7, #0			; COUNTER FOR POSITION IN ARRAY
LOOP LDR R5,[R0],#4     ; GET ARRAY ELEMENT IN R5 AND UPDATE POINTER TO NEXT
	ADD R7,#1 			; POSITION COUNTER UPDATION
	CMP R4,R5			; COMPARISION TO CHECK EQUALITY
	BEQ LABEL1			; BRANCH IF EQUAL TO LABEL1
	SUBS R3,#1
	BNE LOOP
	B HERE
LABEL1 STR R6,[R1], #4 ; STORE 1 IF ELEMENT PRESENT
		STR R7,[R1]		; STORE ELEMENT POSITION IN ARRAY
HERE  B HERE			; STAY HERE
	
INPUT DCD 23,67,89,45,21,80,76,45,34,9  ;INPUT ARRAY	
	
	AREA MYDATA,DATA,READWRITE
POSITION DCD 0                          ; FINDING RESULT IN TERMS OF EXISTENCE AND POSITIONING IN ARRAY
	END

	