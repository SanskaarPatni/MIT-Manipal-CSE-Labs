	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=BCD
	LDR R1,[R0]
	MOV R2,#0
	LDR R5,=ANS
	MOV R4,#0X0F
	MOV R6,#1
	MOV R7,#0X0A
	
LOOP CMP R1,#0
	BEQ STORE
	AND R3,R1,R4
	MLA R2,R3,R6,R2
	MUL R6,R6,R7
	LSR R1,#4
	B LOOP

STORE STR R2,[R5]

STOP B STOP

BCD DCD 0X00001299
	AREA MYDATA,DATA,READWRITE
ANS DCD 0
	END