	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=ARRAY
	LDR R1,=key
	MOV R2,#10
	LDR R4,[R1]
	MOV R6,#0

LOOP LDR R3,[R0]
	CMP R3,R4
	BEQ FOUND
	SUBS R2,#1
	ADD R6,#1
	ADD R0,#4
	BNE LOOP
	
FOUND MOV R5,R6
	
	
STOP B STOP

ARRAY DCD 9,8,7,6,5,4,3,2,1,0
key  DCD 4
	AREA MYDATA,DATA,READWRITE

RESULT DCD 0
	END